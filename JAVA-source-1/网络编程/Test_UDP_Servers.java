/*
	UDP 网络编程
	
	服务器端/接收端
*/

import java.io.*;
import java.net.*;

public class Test_UDP_Servers
{
	public static void main(String args[]) throws Exception
	{
		//定义码头(套接字)
		DatagramSocket ds = new DatagramSocket(5218);	//5218指该码头(套接字) 占用了5218端口;
		
		//定义可以用来接受数据的集装箱(包)
		byte buf[] = new byte[1024];
		DatagramPacket dp = new DatagramPacket(buf, buf.length);
		
		try
		{
			while(true)	
			{
				//在码头上用集装箱接受对方发送过来的数据
				ds.receive(dp);	//注意：本语句执行完毕就意味着，dp数据包中就已经含有了从客户端接收过来的数据
				
				//从集装箱中取出对方发送过来的数据
				ByteArrayInputStream bais = new ByteArrayInputStream(dp.getData());
				DataInputStream dis = new DataInputStream(bais);
				//1. ByteArrayInputStream的内核必须是个字节数组，并且是从该字节数组中读取数据  
				//2. dp.getData()表示把dp集装箱中的数据转化为一个字节数组并返回该字节数组
				System.out.println(dis.readLong());
			}
		}
		catch(Exception e){
			e.printStackTrace();
			ds.close();		//关闭码头
		}
	}
}